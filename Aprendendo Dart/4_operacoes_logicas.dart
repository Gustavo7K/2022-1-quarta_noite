void main() {
  bool a = true;
  bool b = false;
  
  print(a && b); // && => E
  print(a || b); // || => OU
  print(a == b); // verifica se são iguais
  print(a != b); // verifica se são diferentes
  
  
  print("");
  
  int x = 1;
  int y = 3;
  
  print(x > y); //verifica se o lado esquerdo é maior
  print(x < y); //verifica se o lado esquerdo é menor
  
  print(x >= y); //verifica se o lado esquerdo é maior OU igual
  print(x <= y); //verifica se o lado esquerdo é menor OU igual
  
  print(x == y); // verifica se são iguais
  
  
  print(x != y); //verifica se são diferentes

}
